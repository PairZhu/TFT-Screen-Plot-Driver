const unsigned char* getFont(char ch)
{
    static const unsigned char defaultFont[12] = {0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00};
    if (ch >= '0' && ch <= '9')
        return FONTARR[ch - '0'];
    else if (ch >= 'a' && ch <= 'z')
        return FONTARR[ch - 'a' + 10];
    else if (ch >= 'A' && ch <= 'Z')
        return FONTARR[ch - 'A' + 10 + 26];
    else if (ch == '.')
        return FONTARR[62];
    else if (ch == ':')
        return FONTARR[63];
    else if (ch == '-')
        return FONTARR[64];
    else if (ch == '~')
        return FONTARR[65];
    else if (ch == '=')
        return FONTARR[66];
    return defaultFont;
}
